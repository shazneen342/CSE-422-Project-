from sklearn.metrics import ConfusionMatrixDisplay

def evaluate_multiclass(model, name, X_tr, X_te, y_tr, y_te):
    model.fit(X_tr, y_tr)
    y_pred = model.predict(X_te)

    if hasattr(model, "predict_proba"):
        y_prob = model.predict_proba(X_te)
        auc_score = roc_auc_score(y_te, y_prob, multi_class='ovr', average='macro')
    else:
        auc_score = None

    acc   = accuracy_score(y_te, y_pred)
    prec  = precision_score(y_te, y_pred, average='macro', zero_division=0)
    rec   = recall_score(y_te, y_pred, average='macro', zero_division=0)
    f1    = f1_score(y_te, y_pred, average='macro', zero_division=0)

    print(f"\n{'='*40}")
    print(f"  {name}")
    print(f"{'='*40}")
    print(f"Accuracy : {acc:.4f}")
    print(f"Precision: {prec:.4f}")
    print(f"Recall   : {rec:.4f}")
    print(f"F1-score : {f1:.4f}")
    if auc_score: print(f"AUC (OvR): {auc_score:.4f}")
    print("\nClassification Report:")
    print(classification_report(y_te, y_pred, digits=4))

    # Confusion Matrix
    cm = confusion_matrix(y_te, y_pred)
    plt.figure(figsize=(6, 5))
    sns.heatmap(cm, annot=True, fmt='d', cmap='Blues', cbar=False)
    plt.title(f"Confusion Matrix — {name}")
    plt.xlabel("Predicted")
    plt.ylabel("True")
    plt.show()

    return {"Model": name, "Accuracy": acc, "Precision": prec,
            "Recall": rec, "F1": f1, "AUC": auc_score if auc_score else 0}

