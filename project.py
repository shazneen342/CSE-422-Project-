num_samples = min(92, len(X_test_final))
random_indices = np.random.choice(len(X_test_final), num_samples, replace=False)

# Multiclass labels mapping
disease_map = {
    0: "No Disease",
    1: "Mild",
    2: "Moderate",
    3: "Severe",
    4: "Critical"
}

# Convert X_test to numpy for easier indexing if it isn't already
X_test_np = X_test_final.values if hasattr(X_test_final, 'values') else X_test_final

# Header
print(f"{'ID':<7} | {'Actual':<12} | {'DT':<12} | {'LR':<12} | {'NN':<12}")
print('-' * 70)

summary_results = []

for idx in random_indices:
    # Handle y_test indexing (pandas vs numpy)
    if isinstance(y_test, (pd.Series, pd.DataFrame)):
        actual_class = y_test.iloc[idx]
    else:
        actual_class = y_test[idx]

    # Model Predictions
    dt_pred = dt_model.predict(X_test_np[[idx]])[0]
    lr_pred = lr_model.predict(X_test_np[[idx]])[0]

    # Neural Network Prediction (argmax to get the class with highest probability)
    nn_probs = nn_model.predict(X_test_np[[idx]], verbose=0)[0]
    nn_pred = np.argmax(nn_probs)

    # Convert numeric classes to readable labels
    actual = disease_map[actual_class]
    dt_p = disease_map[dt_pred]
    lr_p = disease_map[lr_pred]
    nn_p = disease_map[nn_pred]

    # Print Row
    print(f"{idx:<7} | {actual:<12} | {dt_p:<12} | {lr_p:<12} | {nn_p:<12}")

    # Store for final tally
    summary_results.append({
        'Actual': actual_class,
        'DT': dt_pred,
        'LR': lr_pred,
        'NN': nn_pred
    })

print('-' * 70)

# Final Accuracy Tally
for model_key in ['DT', 'LR', 'NN']:
    correct = sum(1 for r in summary_results if r['Actual'] == r[model_key])
    accuracy = (correct / num_samples) * 100
    print(f'{model_key} Total Correct: {correct}/{num_samples} ({accuracy:.2f}%)')